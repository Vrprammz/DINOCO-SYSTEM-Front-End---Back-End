function seededRandom(seed) {
  let s = seed;
  return () => {
    s = (s * 1664525 + 1013904223) & 0x7fffffff;
    return s / 0x7fffffff;
  };
}

function generateProducts(count) {
  const rng = seededRandom(42);
  const products = [];
  const usedSKUs = new Set();
  let idx = 0;

  while (products.length < count) {
    const tmpl = TEMPLATES[idx % TEMPLATES.length];
    const brandIdx = Math.floor(rng() * tmpl.brands.length);
    const sizeIdx = Math.floor(rng() * tmpl.sizes.length);
    const brand = tmpl.brands[brandIdx];
    const size = tmpl.sizes[sizeIdx];

    const catCode = tmpl.prefix.slice(0, 2).toUpperCase();
    const skuNum = String(products.length + 1).padStart(5, "0");
    const sku = `${catCode}-${skuNum}`;
    if (usedSKUs.has(sku)) { idx++; continue; }
    usedSKUs.add(sku);

    const [minP, maxP] = tmpl.priceRange;
    const price = Math.round((minP + rng() * (maxP - minP)) * 100) / 100;

    const [minS, maxS] = tmpl.stockRange;
    const stock = minS === 0 && maxS === 0 ? null : Math.round(minS + rng() * (maxS - minS));

    const status = rng() > 0.1 ? "active" : "inactive";

    const name = `${tmpl.prefix} ${brand} ${size}`;

    products.push({
      name,
      sku,
      category: tmpl.category,
      description: tmpl.desc(brand, size),
      price,
      unit: tmpl.unit,
      images: [],
      status,
      stock,
      tags: [...tmpl.tags],
      createdAt: new Date(Date.now() - Math.floor(rng() * 90 * 86400000)),
      updatedAt: new Date(),
    });

    idx++;
  }

  return products;
}

async function main() {
  console.log("🔌 Connecting to MongoDB...");
  const client = new MongoClient(MONGODB_URI);
  await client.connect();
  const db = client.db(DB_NAME);

  console.log("🗑️  Deleting existing products...");
  const del = await db.collection("products").deleteMany({});
  console.log(`   Deleted: ${del.deletedCount}`);

  console.log("🏭 Generating 5,000 products...");
  const products = generateProducts(5000);

  console.log("📦 Inserting in batches of 500...");
  let inserted = 0;
  const BATCH = 500;
  for (let i = 0; i < products.length; i += BATCH) {
    const batch = products.slice(i, i + BATCH);
    await db.collection("products").insertMany(batch, { ordered: false });
    inserted += batch.length;
    console.log(`   ${inserted} / ${products.length}`);
  }

  console.log("📊 Creating indexes...");
  await db.collection("products").createIndex({ category: 1, status: 1 });
  await db.collection("products").createIndex({ name: "text", sku: "text", description: "text" });
  await db.collection("products").createIndex({ createdAt: -1 });
  await db.collection("products").createIndex({ sku: 1 }, { unique: true });

  // Stats
  const total = await db.collection("products").countDocuments();
  const byCat = await db.collection("products").aggregate([
    { $group: { _id: "$category", count: { $sum: 1 } } },
    { $sort: { count: -1 } }
  ]).toArray();

  console.log(`\n✅ Done! Total: ${total} products`);
  console.log("📊 By category:");
  for (const c of byCat) {
    console.log(`   ${c._id}: ${c.count}`);
  }

  await client.close();
}

main().catch(err => { console.error("❌ Error:", err); process.exit(1); });
